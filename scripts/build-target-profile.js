#!/usr/bin/env node
"use strict";

const fs = require("fs");

const targetName = process.argv[2];
const reportPath = process.argv[3];
const outputPath = process.argv[4];

if (!targetName || !reportPath || !outputPath) {
  console.error(
    "Usage: node scripts/build-target-profile.js <target-name> <zap-report.json> <output.yaml>"
  );
  process.exit(1);
}

const report = JSON.parse(fs.readFileSync(reportPath, "utf8"));
const firedPluginIds = new Set();
const sites = Array.isArray(report.site) ? report.site : report.site ? [report.site] : [];
for (const site of sites) {
  for (const alert of site.alerts || []) {
    firedPluginIds.add(parseInt(alert.pluginid, 10));
  }
}

const passiveFired = [...firedPluginIds]
  .filter((id) => id < 40000 && id !== 2)
  .sort((a, b) => a - b);
const activeFired = [...firedPluginIds]
  .filter((id) => id >= 40000 || id === 2)
  .sort((a, b) => a - b);

const IRRELEVANT_ACTIVE_FOR_REST = [
  { id: 40014, name: "Cross Site Scripting (Persistent)", reason: "No stored HTML rendering" },
  { id: 40026, name: "Cross Site Scripting (DOM Based)", reason: "No browser execution" },
  { id: 90019, name: "Server Side Code Injection", reason: "ASP.NET specific" },
  { id: 10045, name: "Source Code Disclosure - /WEB-INF", reason: "Java WAR specific" },
  { id: 90034, name: "Cloud Metadata Attack", reason: "Not in cloud (Docker internal)" },
  { id: 10047, name: "HTTPS Content via HTTP", reason: "Internal network, no HTTPS" },
];

const profile = {
  targetName,
  generatedFrom: reportPath,
  generatedAt: new Date().toISOString(),
  firedPassiveRules: passiveFired,
  firedActiveRules: activeFired,
  disabledActiveRules: IRRELEVANT_ACTIVE_FOR_REST,
};

const yaml = [
  "# Generated by scripts/build-target-profile.js — review before committing",
  `# target: ${targetName}`,
  `generatedFrom: ${JSON.stringify(reportPath)}`,
  `generatedAt: ${JSON.stringify(profile.generatedAt)}`,
  "firedPassiveRules: [" + passiveFired.join(", ") + "]",
  "firedActiveRules: [" + activeFired.join(", ") + "]",
  "disabledActiveRules:",
  ...IRRELEVANT_ACTIVE_FOR_REST.map(
    (r) =>
      `  - id: ${r.id} # ${r.name} — ${r.reason}`
  ),
  "",
].join("\n");

fs.writeFileSync(outputPath, yaml, "utf8");
console.error("Wrote", outputPath);
